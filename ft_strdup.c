#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	size_t	len;
	char	*dest;

	len = 0;
	i = 0;
	while (s1[len])
		len++;
	dest = (char *)malloc(len * sizeof(char) + 1);
	if (!dest)
		return (0);
	while (i < len)
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
